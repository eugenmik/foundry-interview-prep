def t(lang: str, key: str) -> str:
    """Translate a key into the given language, falling back to English."""
    return _T.get(lang, _T["en"]).get(key, _T["en"].get(key, key))


def language_name(lang: str) -> str:
    """Full English name of the language, for use inside prompts."""
    return LANGUAGE_NAMES.get(lang, "English")
